let readmoretext1 = document.getElementById("readmoretext1");
let textcontant1 = document.getElementById("textcontant1");

readmoretext1.onclick = () => {
    if (textcontant1.style.display === "none") {
        textcontant1.style.display = "inline";
        readmoretext1.textContent = "Read Less";
    } else {
        textcontant1.style.display = "none";
        readmoretext1.textContent = "Read More";
    }
};

